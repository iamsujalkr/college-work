import math

print("Math Module Functions Demonstration\n")

number = -5
print(f"Absolute value of {number}: {math.fabs(number)}")

n = 5
print(f"Factorial of {n}: {math.factorial(n)}")

number = 16
print(f"Square root of {number}: {math.sqrt(number)}")

base, exp = 2, 3
print(f"{base} raised to the power of {exp}: {math.pow(base, exp)}")

value = 100
print(f"Natural logarithm of {value}: {math.log(value)}")
print(f"Base 10 logarithm of {value}: {math.log10(value)}")

exponent = 2
print(f"e raised to the power of {exponent}: {math.exp(exponent)}")

print(f"Value of Pi: {math.pi}")
print(f"Value of Euler's number (e): {math.e}")


